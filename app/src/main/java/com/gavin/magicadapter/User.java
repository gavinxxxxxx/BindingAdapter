package com.gavin.magicadapter;

/**
 * 测试 Model - 用户
 *
 * @author gavin.xiong 2017/3/31
 */
public class User {

    private long id;
    private String name;
    private String head;

    public long getId() {
        return id;
    }

    public void setId(long id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getHead() {
        return head;
    }

    public void setHead(String head) {
        this.head = head;
    }
}
